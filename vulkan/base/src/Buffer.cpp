Buffer::Buffer(std::shared_ptr<LogicalDevice> logicalDevice, std::shared_ptr<CommandPool> commandPool, std::shared_ptr<SwapChain> swapChain, std::shared_ptr<RenderPass> renderPass, std::shared_ptr<LoadModel> loadModel) : m_LogicalDevice(logicalDevice), m_CommandPool(commandPool),
    m_SwapChain(swapChain), m_RenderPass(renderPass), m_LoadModel(loadModel)
{
    
}

void Buffer::cleanupSwapChain() {
    auto device = m_LogicalDevice->GetDevice();
    auto swapChainImages = m_SwapChain->GetSwapChainImages();
    for (size_t i = 0; i < swapChainImages.size(); i++) {
        vkDestroyBuffer(device, uniformBuffers[i], nullptr);
        vkFreeMemory(device, uniformBuffersMemory[i], nullptr);
    }
}

void Buffer::cleanup() {
    auto device = m_LogicalDevice->GetDevice();
    
    vkDestroyBuffer(device, indexBuffer, nullptr);
    vkFreeMemory(device, indexBufferMemory, nullptr);

    vkDestroyBuffer(device, vertexBuffer, nullptr);
    vkFreeMemory(device, vertexBufferMemory, nullptr);
}
